'''Dictionaries are a data structure that allows you to store data in key-value pairs.  
Dictionaries are mutable, meaning that you can change the data in a dictionary after it has been created.

These are used to solve problems like finding the frequency of characters in a string, 
finding the frequency of words in a paragraph '''

#declaring a dictionary
my_dict={}                                  #empty dictionary

my_dict = {'a': 1, 'b': 2, 'c': 3}


for key in my_dict.keys():
    print(key)


